package homework7.part2;

public class Simple {

    public static void summing(double firstnumber, double secondnumber){
        double result = firstnumber + secondnumber;
        System.out.println("summing result :" + result);
    }
    public static void subtraction(double firstnumber, double secondnumber){
        double result = firstnumber - secondnumber;
        System.out.println("subtraction result :" + result);
    }
    public static void multiplication(double firstnumber, double secondnumber){
        double result = firstnumber * secondnumber;
        System.out.println("multiplication result :" + result);
    }
    public static void division(double firstnumber, double secondnumber){
        double result = firstnumber / secondnumber;
        System.out.println("division result :" + result);
    }
    public static void remainderOfdivision(double firstnumber, double secondnumber){
        double result = firstnumber % secondnumber;
        System.out.println("remainderOfdivision result :" + result);
    }
}
